import UIKit

extension UIColor {
    static func kairosGreen() -> UIColor {
        return UIColor(red:0.18, green:0.71, blue:0.46, alpha:1.0)
    }
    
    static func kairosGrey() -> UIColor {
        return UIColor(red:0.17, green:0.20, blue:0.27, alpha:1.0)
    }
    
    static func kairosDarkGrey() -> UIColor {
        return UIColor(red:0.13, green:0.15, blue:0.20, alpha:1.0)
    }
    
    static func kairosRed() -> UIColor {
        return UIColor(red:0.85, green:0.00, blue:0.35, alpha:1.0)
    }
    
    static func kairosBlue() -> UIColor {
        return UIColor(red:0.07, green:0.46, blue:0.89, alpha:1.0)
    }
}
